let wage = 20.000

// +, -, *, /, **(exponencial)

console.log(wage + wage) // 40
console.log(wage - wage) // 0
console.log(wage * wage) // 400
console.log(wage / wage) // 1
console.log(wage ** wage) // 1.048576e+26

